package it.polimi.ingsw.psp44.util;

public class Card {
    private int id;
    private String title;
    private String subtitle;
    private String description;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getSubtitle() {
        return subtitle;
    }

    public String getDescription() {
        return description;
    }
}
